package br.com.suellenoliveiraprog.sistemaaiko.model;

public class EquipamentoState {
    
    private long id;
    private String nome;
    private String cor;
    
    public long getId() {
        return id;
    }
    
    public void setId(long id) {
        this.id = id;
    }
    
    public String getNome() {
        return nome;
    }
    
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public String getCor() {
        return cor;
    }
    
    public void setCor(String cor) {
        this.cor = cor;
    }

    
}
